      <h4>Overview</h4>
      <div class="contentarea">
	Macro-programming is the task of specifying the global behavior of a distributed system and deriving the behaviors of individual nodes. This is the opposite of specifying local node behaviors and deriving global, emergent behaviors. We are designing a programming abstraction which would enable the user to program a sensor network as if it were a single computer. The user will write a script describing the behavior of the system at the application level using a high-level language such as Java. Our compiler/run-time system will decompose the user generated script into scripts to be executed on each device within the sensor network. This work is building on our previous experience with Macro-programming systems like <a href="http://www.cs.virginia.edu/~whitehouse/research/marionette/">Marionette</a> and <a href="http://www.cs.virginia.edu/~whitehouse/research/semanticStreams/">Semantic Streams</a>.
