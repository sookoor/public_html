      <h4>Overview</h4>
      <div class="contentarea">
	In LUSTER, a fleet of sensors coordinate communications using LiteTDMA, a low-power cluster-based MAC protocol. They measure the complex light environment in thickets and are open to additional ecological parameters, such as temperature and CO2. LUSTER has been deployed and evaluated in laboratory, forested, and barrier island environments. It includes new sensor hardware designs: (a) "SolarDust," a hybrid multichannel energy harvesting and sensing device; (b) "Medusa," a spatially reconfigurable light sensor; (c) a removable SD card storage node; and, (d) in-situ user interface tool for deployment time validation. In cooperation with environmental scientists, we deployed LUSTER on a barrier island off the Eastern Shore of Virginia.      

